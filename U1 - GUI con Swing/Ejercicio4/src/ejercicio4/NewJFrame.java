    public String[] mezclarNombres(String nombre1, String nombre2){
        String parte1 = nombre1.substring(0, nombre1.length() / 2);
        String parte2 = nombre2.substring(0, nombre2.length() / 2);
        
        String nombreMix1 = parte1 + parte2;
        String nombreMix2 = parte2 + parte1;
        String nombreMix3 = nombre1.substring(nombre1.length() / 2) + nombre2.substring(0, nombre2.length() / 2);
        String nombreMix4 = nombre2.substring(nombre2.length() / 2) + nombre1.substring(0, nombre1.length() / 2);
       
        return new String[]{nombreMix1.toUpperCase(), nombreMix2.toUpperCase(), nombreMix3.toUpperCase(), nombreMix4.toUpperCase()};
    }
    
